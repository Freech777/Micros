LIST p=16F877A
INCLUDE <p16F877A.inc>

__CONFIG _XT_OSC & _WDT_OFF & _PWRTE_ON & _BODEN_OFF & _LVP_OFF

ORG 0x00

CBLOCK 0x20
    TEMP
ENDC

BSF STATUS, RP0
CLRF TRISC
MOVLW b'00000001'
MOVWF TRISA
MOVLW b'10000000'
MOVWF ADCON1
MOVLW .25
MOVWF PR2
BCF STATUS, RP0

CLRF PORTC

MOVLW b'00000100'
MOVWF T2CON

MOVLW b'00001100'
MOVWF CCP1CON

MOVLW .13
MOVWF CCPR1L

MOVLW b'01000001'
MOVWF ADCON0

MOVLW .50
MOVWF TEMP
STAB:
    NOP
    DECFSZ TEMP,F
    GOTO STAB

MAIN:
    BSF ADCON0, GO_DONE
WAIT:
    BTFSC ADCON0, GO_DONE
    GOTO WAIT

    ; ADC directo a duty — sin lógica, sin umbral
    MOVF ADRESH, W
    MOVWF CCPR1L

    GOTO MAIN

END