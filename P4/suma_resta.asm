LIST p=16F877A
INCLUDE <p16F877A.inc>

__CONFIG  _XT_OSC & _WDT_OFF & _PWRTE_ON & _BODEN_OFF & _LVP_OFF



ORG 0x00


CBLOCK 0x20
VAR
VAR2

ENDC

CONFIGURACION
BSF STATUS, RP0
MOVLW b'00011111'
MOVWF TRISB
MOVLW 0x00
MOVWF TRISC
BCF STATUS, RP0
MOVLW 0x00
MOVWF PORTC

MOVLW 0x01
MOVWF VAR2


MAIN
BTFSC   PORTB,0
GOTO RB0_SUMA3

BTFSC   PORTB,1
GOTO RB1_RESTA2


BTFSC   PORTB,2
GOTO RB2_OFF


BTFSC   PORTB,3
GOTO RB3_ON1

BTFSC   PORTB,4
GOTO RB4_OFF1

goto MAIN

;Suma de 3 en 3 en hexadecimal para pasarlo a binario y mostrarlo en el puerto C
RB0_SUMA3
INCF VAR, F
INCF VAR, F
INCF VAR, F

MOVF VAR, W
MOVWF PORTC

GOTO MAIN

;Resta lo que haya en el puerto C de 2 en 2
RB1_RESTA2
DECF VAR, F
DECF VAR, F

MOVF VAR, W
MOVWF PORTC
GOTO MAIN

;Apaga todos los leds de el puerto C
RB2_OFF
MOVLW 0x00
MOVWF PORTC
goto MAIN

;Enciende led por led en el puerto C
RB3_ON1
MOVF VAR2,W
MOVWF PORTC


RLF VAR2, F
BSF VAR2, W

GOTO MAIN


;Apaga led por led en el puerto C
RB4_OFF1
MOVF VAR2, W
MOVWF PORTC

BCF STATUS, C
RRF VAR2, F
GOTO MAIN
END